// Función para obtener un poder aleatorio
export const getRandomPower = () => {
  return PODERES_ABSURDOS[Math.floor(Math.random() * PODERES_ABSURDOS.length)];
};

// Función para obtener un problema aleatorio
export const getRandomProblem = () => {
  return PROBLEMAS_COTIDIANOS[Math.floor(Math.random() * PROBLEMAS_COTIDIANOS.length)];
};

// Función para generar una misión completa (poder + problema)
export const generateMission = () => {
  return {
    poder: getRandomPower(),
    problema: getRandomProblem(),
    timestamp: new Date().toISOString(),
  };
};

// Generador de nombres de superhéroe basado en el poder
export const generateHeroName = (poder) => {
  const palabras = poder.toLowerCase().split(' ');
  const primeraPalabra = palabras[0];
  const segundaPalabra = palabras[palabras.length - 1];
  
  const nombres = [
    `El ${primeraPalabra.charAt(0).toUpperCase() + primeraPalabra.slice(1)}`,
    `Capitán ${segundaPalabra.charAt(0).toUpperCase() + segundaPalabra.slice(1)}`,
    `Super ${primeraPalabra.charAt(0).toUpperCase() + primeraPalabra.slice(1)}`,
    `Maestro ${segundaPalabra.charAt(0).toUpperCase() + segundaPalabra.slice(1)}`,
    `El Héroe de ${primeraPalabra.charAt(0).toUpperCase() + primeraPalabra.slice(1)}`,
  ];
  
  return nombres[Math.floor(Math.random() * nombres.length)];
};

