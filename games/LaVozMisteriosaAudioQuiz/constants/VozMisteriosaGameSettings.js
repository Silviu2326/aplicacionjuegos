// Función para obtener preguntas aleatorias de una categoría
export const getRandomQuestion = (category) => {
  const questionsInCategory = QUESTIONS.filter(q => q.category === category);
  if (questionsInCategory.length === 0) return null;
  
  const randomIndex = Math.floor(Math.random() * questionsInCategory.length);
  return questionsInCategory[randomIndex];
};

// Función para obtener preguntas aleatorias de múltiples categorías
export const getRandomQuestionFromCategories = (categories) => {
  if (categories.length === 0) return null;
  
  // Filtrar preguntas de las categorías seleccionadas (todas tienen audio simulado)
  const questionsInCategories = QUESTIONS.filter(
    q => categories.includes(q.category) && q.audioUrl
  );
  if (questionsInCategories.length === 0) return null;
  
  const randomIndex = Math.floor(Math.random() * questionsInCategories.length);
  return questionsInCategories[randomIndex];
};

