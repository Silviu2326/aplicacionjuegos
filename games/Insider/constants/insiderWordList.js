export const getRandomWordFromCategory = (category) => {
  const pack = WORD_LIST[category];
  if (!pack || !pack.words.length) return null;
  const randomIndex = Math.floor(Math.random() * pack.words.length);
  return pack.words[randomIndex];
};

export const getAllWords = () => {
  const allWords = [];
  Object.values(WORD_LIST).forEach((pack) => {
    allWords.push(...pack.words);
  });
  return allWords;
};

export const getRandomWord = () => {
  const allWords = getAllWords();
  const randomIndex = Math.floor(Math.random() * allWords.length);
  return allWords[randomIndex];
};

export const getRandomCategory = () => {
  const categories = Object.keys(WORD_LIST);
  const randomIndex = Math.floor(Math.random() * categories.length);
  return categories[randomIndex];
};

export const getCategoryInfo = (category) => {
  return WORD_LIST[category] || null;
};

export const getAllCategories = () => {
  return Object.keys(WORD_LIST).map(key => ({
    key,
    name: WORD_LIST[key].name,
    wordCount: WORD_LIST[key].words.length
  }));
};

